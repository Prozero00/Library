    public int id() {
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        int id = -1; // Initialize id with a default value

        try {
            Statement stmt = con.createStatement();
            String st = "SELECT * FROM USERS WHERE USERNAME='" + username + "' AND PASSWORD='" + password + "'"; // Retrieve username and password from users
            ResultSet rs = stmt.executeQuery(st); // Execute query

            if (rs.next()) {
                id = rs.getInt("UID"); // Assign the retrieved UID to id
                System.out.print(id);
            }
        } catch (Exception e) {
            // Handle the exception, e.g., log or display an error message
        }

        return id;
    }

