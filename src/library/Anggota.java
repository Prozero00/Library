    public void LoadTable() {
        int c;

        try {
            pst = con.prepareStatement("SELECT * FROM anggota JOIN prodi USING(id_prodi) ORDER BY id_anggota");
            rs = pst.executeQuery();

            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();

            DefaultTableModel model = (DefaultTableModel) tblAnggota.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("id_anggota"));
                    v2.add(rs.getString("NIM"));
                    v2.add(rs.getString("nama_anggota"));
                    v2.add(rs.getString("nama_prodi"));
                    v2.add(rs.getString("no_telepon"));
                    v2.add(rs.getString("email"));
                    v2.add(rs.getString("alamat"));
                }
                model.addRow(v2);
            }
        } catch (Exception e) {
        }

    }

    public void LoadProdi() {
        String sql = "SELECT * FROM prodi";
        try {
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                String name = rs.getString("nama_prodi");
                cboProdi.addItem(name);
            }
        } catch (Exception e) {
        }
    }


    public void Search(String str) {
        DefaultTableModel model = (DefaultTableModel) tblAnggota.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        tblAnggota.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(str));
    }

