    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        String nama = txtName.getText();
        String jekel = cboJekel.getSelectedItem().toString();
        String alamat = txtAlamat.getText();

        try {

            pst = con.prepareStatement("INSERT INTO staf(nama_staf,jekel,alamat) VALUES(?,?,?)");
            pst.setString(1, nama);
            pst.setString(2, jekel);
            pst.setString(3, alamat);
            int k = pst.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Staf berhasil ditambahkan");
                txtName.setText("");
                cboJekel.setSelectedIndex(0);
                txtAlamat.setText("");
                LoadTable();
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String nama = txtName.getText();
        String jekel = cboJekel.getSelectedItem().toString();
        String alamat = txtAlamat.getText();

        DefaultTableModel model = (DefaultTableModel) tblStaf.getModel();
        int selectIndex = tblStaf.getSelectedRow();

        int id = Integer.parseInt(model.getValueAt(selectIndex, 0).toString());

        try {
            pst = con.prepareStatement("UPDATE staf SET nama_staf=?,jekel=?,alamat=? WHERE id_staf=?");
            pst.setInt(4, id);
            pst.setString(1, nama);
            pst.setString(2, jekel);
            pst.setString(3, alamat);
            int k = pst.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Staf berhasil diupdate");
                txtName.setText("");
                cboJekel.setSelectedIndex(0);
                txtAlamat.setText("");
                LoadTable();
                btnInsert.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

