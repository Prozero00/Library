    public void LoadTable() {
        int c;

        try {
            pst = con.prepareStatement("SELECT * FROM staf ORDER BY id_staf");
            rs = pst.executeQuery();

            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();

            DefaultTableModel model = (DefaultTableModel) tblStaf.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("id_staf"));
                    v2.add(rs.getString("nama_staf"));
                    v2.add(rs.getString("jekel"));
                    v2.add(rs.getString("alamat"));
                }
                model.addRow(v2);
            }
        } catch (Exception e) {
        }

    }

    public void Search(String str) {
        DefaultTableModel model = (DefaultTableModel) tblStaf.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        tblStaf.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(str));
    }

