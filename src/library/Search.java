    public void Connect() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/db_perpustakaan", "root", "");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Anggota.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Anggota.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void LoadTable() {
        int c;

        try {
            pst = con.prepareStatement("SELECT * FROM buku");
            rs = pst.executeQuery();

            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();

            DefaultTableModel model = (DefaultTableModel) tblAnggota.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("id_buku"));
                    v2.add(rs.getString("judul"));
                    v2.add(rs.getString("pengarang"));
                    v2.add(rs.getString("penerbit"));
                    v2.add(rs.getString("tahun_terbit"));
                    v2.add(rs.getString("jumlah_buku"));
                }
                model.addRow(v2);
            }
        } catch (Exception e) {
        }

    }

    public void Search(String str) {
        DefaultTableModel model = (DefaultTableModel) tblAnggota.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        tblAnggota.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(str));
    }

